first_number = int(input("შეიყვანეთ პირველი რიცხვი: ")) 
second_number = int(input("შეიყვანეთ მეორე რიცხვი: ")) 


if first_number > 30 and second_number < 40:
    print("პირველი რიცხვი მეტია 30-ზე და მეორე ნაკლებია 40-ზე.")
else:
    print("პირველი რიცხვი არ არის მეტია 30-ზე ან მეორე რიცხვი არ არის ნაკლები 40-ზე.")